    private void tblEspecialidadMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEspecialidadMouseClicked
        if (evt.getButton()==1){
            int fila = tblEspecialidad.getSelectedRow();
            cnx.Conecta();
            try{
                Habilitar();                               
                String SQL = "Select * from especialidad where idespecialidad = " + tblEspecialidad.getValueAt(fila, 0);
                ps = cnx.conn.prepareStatement(SQL);
                rs = ps.executeQuery();
                while (rs.next()){
                txtEspecialidad.setText(rs.getString("descripcion_especialidad"));
                }                
            } catch(SQLException e){
                JOptionPane.showMessageDialog(null, "Error Especialidad Mouse Cliked: " + e.getMessage());
            } finally {
                BotonesClick();
                cnx.Desconecta();
            }
        }
    }//GEN-LAST:event_tblEspecialidadMouseClicked

