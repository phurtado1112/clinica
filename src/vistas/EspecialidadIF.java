    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        if (validar()==true){        
        int i = JOptionPane.showConfirmDialog(null, "Desea Actualizar?","Confirmar",
            JOptionPane.OK_CANCEL_OPTION,JOptionPane.ERROR_MESSAGE);
        if(i==JOptionPane.OK_OPTION){                
                int fila = tblEspecialidad.getSelectedRow();
                e.setDescripcion_especialidad(txtEspecialidad.getText().trim());
                e.setIdespecialidad(Integer.parseInt(tblEspecialidad.getValueAt(fila, 0).toString())); 
                e.actualizarEspecialidad();
        }
        LlenarTabla();
        limpiar();
        Deshabilitar();
        BotonesInicio();
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

